MARKER_PREFIX = "<!-- BEGIN AUTO-GENERATED"
MARKER_SUFFIX = "-->"


@dataclass(frozen=True)
class GeneratedSection:
    name: str

    @property
    def begin(self) -> str:
        return f"{MARKER_PREFIX} {self.name} {MARKER_SUFFIX}"

    @property
    def end(self) -> str:
        return f"<!-- END AUTO-GENERATED {self.name} {MARKER_SUFFIX}"


