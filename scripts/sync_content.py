def write_catalog_json(
    catalog: content_model.ContentCatalog,
    destination: Path | None = None,
) -> Path:
    destination = destination or content_model.SITE_ROOT / "generated" / "catalog.json"
    destination.parent.mkdir(parents=True, exist_ok=True)
    payload = catalog_to_dict(catalog)
    serialized = json.dumps(payload, indent=2, sort_keys=True) + "\n"
    destination.write_text(serialized, encoding="utf-8")
    return destination


def sync_catalog_pages(catalog: content_model.ContentCatalog | None = None) -> content_model.ContentCatalog:
    catalog = catalog or content_model.load_catalog()
    replace_generated_section(
        content_model.SITE_ROOT / "lectures.qmd",
        render_lectures_page(catalog),
        LECTURES_SECTION,
    )
    replace_generated_section(
        content_model.SITE_ROOT / "schedule.qmd",
        render_schedule_page(catalog),
        SCHEDULE_SECTION,
    )
    replace_generated_section(
        content_model.SITE_ROOT / "archive.qmd",
        render_archive_page(catalog),
        ARCHIVE_SECTION,
    )
    replace_generated_section(
        content_model.SITE_ROOT / "index.qmd",
        render_offering_banner(catalog),
        OFFERING_BANNER_SECTION,
    )
    syllabus_path = content_model.SITE_ROOT / "syllabus.qmd"
    if syllabus_path.exists():
        replace_generated_section(
            syllabus_path,
            render_syllabus_logistics(catalog),
            SYLLABUS_LOGISTICS_SECTION,
        )
    assignments_path = content_model.SITE_ROOT / "assignments.qmd"
    if assignments_path.exists():
        replace_generated_section(
            assignments_path,
            render_assignments_page(catalog),
            ASSIGNMENTS_SECTION,
        )
    write_catalog_json(catalog)
    return catalog


def sync_gallery_pages() -> None:
    gallery_path = content_model.SITE_ROOT / "generated" / "gallery.json"
    if not gallery_path.exists():
        raise RuntimeError(
            f"{gallery_path.relative_to(content_model.SITE_ROOT)} not found; "
            "run scripts/build_interactives.py first"
        )
    tools_path = content_model.SITE_ROOT / "tools.qmd"
    if not tools_path.exists():
        raise RuntimeError(
            f"{tools_path.relative_to(content_model.SITE_ROOT)} does not exist"
        )

    entries = gallery.load_gallery_json(gallery_path)
    replace_generated_section(
        tools_path,
        render_tools_page(entries),
        TOOLS_SECTION,
    )


def run_phase(phase: str) -> None:
    if phase == "catalog":
        normalized = normalize_lecture_sources()
        catalog = sync_catalog_pages()
        print(
            f"Synchronized catalog: {normalized} normalized lecture(s), "
            f"{len(catalog.lectures)} lecture(s), "
            f"{len(catalog.assignments)} assignment(s), "
            f"{len(catalog.offerings)} offering(s)."
        )
        return
    if phase == "gallery":
        sync_gallery_pages()
        entries = gallery.load_gallery_json()
        print(f"Gallery phase complete: {len(entries)} tool(s) published.")
        return
    raise SystemExit(f"unknown sync phase: {phase}")


def main(argv: list[str] | None = None) -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--phase",
        choices=("catalog", "gallery"),
        default="catalog",
        help="sync phase to run (default: catalog)",
    )
    args = parser.parse_args(argv)
    run_phase(args.phase)


if __name__ == "__main__":
    main()
